''' - Qual a diferença entre usar aspas simples (') e aspas duplas (") ? '''
# Ambas armazenam a mesma string
print('Olá, "Mundo"!') # quando precisar imprimir aspas duplas use aspas simples no print
print("Olá, 'Mundo'!") # quando precisar imprimir aspas simples use aspas duplas no print

# Outro opção:
print('Olá, \'Mundo\'') # utilizar a barra e a apas \'
print("Olá, \"Mundo\"") # utilizar a barra e a apas \"